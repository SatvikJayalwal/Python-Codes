#escape characters
# To insert characters that are illegal in a string 

#  \'
t="it\'s all right"
print(t)

#  \\
t2="this will insert only one (\\)back slash"
print(t2)

#  \n
t3="this will print in \nnew line"
print(t3)

#  \r
t4="this \rwill take the cursor and print the line from where it written"
print(t4)

#  \t
t5="this will\tput a tab"
print(t5)

#  \b
t6="this will put a back \bspace in the line"
print(t6)

